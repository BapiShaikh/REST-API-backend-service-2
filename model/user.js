const mongoose = require("mongoose");


const postSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
});

const users = new mongoose.model("users", postSchema);

module.exports = users